struct Vertex {
    glm::vec3 pos;
    glm::vec3 color;
    glm::vec2 texCoord;

    static VkVertexInputBindingDescription getBindingDescription() {
        VkVertexInputBindingDescription bindingDescription{};
        bindingDescription.binding = 0;
        bindingDescription.stride = sizeof(Vertex);
        bindingDescription.inputRate = VK_VERTEX_INPUT_RATE_VERTEX;

        return bindingDescription;
    }

    static std::array<VkVertexInputAttributeDescription, 3> getAttributeDescriptions() {
        std::array<VkVertexInputAttributeDescription, 3> attributeDescriptions{};

        attributeDescriptions[0].binding = 0;
        attributeDescriptions[0].location = 0;
        attributeDescriptions[0].format = VK_FORMAT_R32G32B32_SFLOAT;
        attributeDescriptions[0].offset = offsetof(Vertex, pos);

        attributeDescriptions[1].binding = 0;
        attributeDescriptions[1].location = 1;
        attributeDescriptions[1].format = VK_FORMAT_R32G32B32_SFLOAT;
        attributeDescriptions[1].offset = offsetof(Vertex, color);

        attributeDescriptions[2].binding = 0;
        attributeDescriptions[2].location = 2;
        attributeDescriptions[2].format = VK_FORMAT_R32G32_SFLOAT;
        attributeDescriptions[2].offset = offsetof(Vertex, texCoord);

        return attributeDescriptions;
    }
    //
    bool operator==(const Vertex& other) const {
        return pos == other.pos && color == other.color && texCoord == other.texCoord;
    }
};
// 定义一个命名空间std，这是C++标准库的命名空间
// vertex类型的哈希值是指将一个vertex对象（通常表示一个网格中的顶点）转换为一个整数值，以便在哈希表中存储或查找。
// 哈希值应该满足一定的性质，例如相等的对象应该有相同的哈希值，不相等的对象应该尽量有不同的哈希值，哈希值应该均匀分布等。
// 
// std::unordered_map<Vertex, uint32_t> points;这种写法可以创建一个以Vertex为键，以uint32_t为值的哈希表。
// 但是，这要求Vertex类型有一个可用的哈希函数相等和比较函数。如果没有提供这两个函数，那么编译器会报错。
// 一种解决方法是提供一个自定义的哈希函数类，比如struct VertexHash函数类，并将其作为第三个模板参数传递给std::unordered_map。
// 另一种解决方法是对std::hash<Vertex>进行特化，也就是提供一个专门针对Vertex类型的哈希函数类。
//  这样就可以省略第三个模板参数，因为std::unordered_map会默认使用std::hash作为哈希函数类。
// 
/*namespace std {
struct VertexHash{                   
    size_t operator()(Vertex const& vertex) const { 
        return ((hash<glm::vec3>()(vertex.pos) ^
            (hash<glm::vec3>()(vertex.color) << 1)) >> 1) ^
            (hash<glm::vec2>()(vertex.texCoord) << 1);
    }
};
}*/
// 
// struct VertexHash与template<> struct hash<Vertex>的区别在于，前者是一个普通的类模板，后者是一个对std::hash<Vertex>的全特化。
// 全特化意味着对于某个具体的类型（这里是Vertex），提供一个特定的实现。
//  struct VertexHash只能作为std::unordered_map的第三个模板参数来使用，
//  而template<> struct hash<Point>可以让std::unordered_map自动使用它作为默认的哈希函数类。
// 
// hash<int>()是一个函数对象，也就是一个重载了()运算符的类或结构体的实例。
//  重载了()运算符意味着我们可以让一个类或结构体的对象像函数一样被调用，只要在类或结构体中定义一个名为operator()的成员函数，并指定它的参数和返回值。
// 
// 
    /*
    class Adder {
    public:
        // 定义一个重载了()运算符的成员函数
        int operator()(int a, int b) {
            return a + b;
        }
    };
    
    // 创建一个函数对象
    Adder add;
    
    // 调用函数对象
    int sum = add(3, 4); // 等价于 add.operator()(3, 4)
    */
// C++中有一些现成的哈希函数，例如std::hash，也可以自定义哈希函数
namespace std {
    template<> struct hash<Vertex> {                    // 定义一个模板特化，用于计算Vertex类型的哈希值
        size_t operator()(Vertex const& vertex) const { // 定义一个函数对象，接受一个Vertex类型的常量引用作为参数
            // 返回一个size_t类型的值，表示Vertex对象的哈希值
            // 这里使用了位运算符和glm库提供的哈希函数
            // 如果我们有两个整数16843009和16842752（十六进制为0x01010101和0x01010000）
            //按位与(&)：将两个整数的每一位进行逻辑与运算，如果两个位都为1，则结果为1，否则为0。
            // 例如，16843009 & 16842752 = 16842752（十六进制为0x01010000）。
            //按位或(| )：将两个整数的每一位进行逻辑或运算，如果两个位中有一个为1，则结果为1，否则为0。
            // 例如，16843009 | 16842752 = 16843009（十六进制为0x01010101）。
            //按位异或(^)：将两个整数的每一位进行逻辑异或运算，如果两个位不同，则结果为1，否则为0。
            // 例如，16843009 ^ 16842752 = 257（十六进制为0x00000101）。
            //按位左移(<< )：将一个整数的所有位向左移动指定的位数，并用0填充空出的位置。
            // 例如，16843009 << 1 = 33686018（十六进制为0x02020202）。
            //按位右移(>> )：将一个整数的所有位向右移动指定的位数，并用符号位填充空出的位置。
            // 例如，16843009 >> 1 = 8421504（十六进制为0x00808080）。
            // 如何设计哈希函数
            return ((hash<glm::vec3>()(vertex.pos) ^ 
                (hash<glm::vec3>()(vertex.color) << 1)) >> 1) ^ 
                (hash<glm::vec2>()(vertex.texCoord) << 1);
        }
    };
}
